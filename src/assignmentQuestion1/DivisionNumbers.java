package assignmentQuestion1;

public class DivisionNumbers {
	int n1;
	int n2;
	
	void divisionOfNumbers() {
		int q = n1 / n2;
		System.out.println("Quotient of the division operation of " + n1 + " and " + n2 + " is " + q);
		int r = n1 % n2;
		System.out.println("Remainder of the division operation of " + n1 + " and " + n2 + " is " + r);
	}

}
